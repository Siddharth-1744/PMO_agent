import os
import requests
from requests.auth import HTTPBasicAuth
from dotenv import load_dotenv
load_dotenv()

def _base_url() -> str:
    """
    Ensures we always use base like https://your-domain.atlassian.net
    """
    url = (os.getenv("JIRA_BASE_URL") or "").strip()
    if not url:
        raise ValueError("JIRA_BASE_URL is missing in .env")

    # Remove trailing slash if present
    url = url.rstrip("/")

    # If someone accidentally put /jira at end, remove it
    if url.endswith("/jira"):
        url = url[:-5]

    return url


def _auth() -> HTTPBasicAuth:
    email = (os.getenv("JIRA_EMAIL") or "").strip()
    token = (os.getenv("JIRA_API_TOKEN") or "").strip()
    if not email or not token:
        raise ValueError("JIRA_EMAIL or JIRA_API_TOKEN missing in .env")
    # Jira Cloud basic auth uses email + API token (password auth is deprecated)
    return HTTPBasicAuth(email, token)


